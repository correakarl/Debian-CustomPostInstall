      section "Reinstalacion"
      remove_profile_packages
      run_install_mode
      ;;
    remove)
      remove_profile_packages
      ;;
    remove-category)
      remove_category_packages_v2
      ;;
    clean)
      section "Limpieza"
      module_debug_clean
      ;;
    clean-obsolete)
      section "Limpieza de reemplazados"
      module_debug_clean
      ;;
    clean-duplicates)
      clean_duplicates_from_catalog_v2
      ;;
    clean-files)
      cleanup_temp_and_downloads_v2
      ;;
    optimize)
      section "Optimizacion"
      module_system_core
      ;;
    updates-cron)
      process_updates_and_cron_v2
      ;;
    remove-cron)
      process_remove_updates_cron_v2
      ;;
    logs)
      show_log_tail 120
      ;;
    refs)
      show_official_references_v2
      ;;
    health)
      health_panel_v2
      ;;
    verify)
      verify_profile_integrity
      ;;
