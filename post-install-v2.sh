      ;;
  esac

  case "${ACTION}" in
    install)
      run_install_mode
      ;;
    check-fix)
      check_and_fix_profile
      ;;
    configure)
      section "Configuracion"
      run_catalog_profile_actions_v2 "${PROFILE}" pre
      module_ux_light
      UX_LIGHT_RAN_V2=true
      show_catalog_plan_for_profile_v2 "${PROFILE}"
      module_dev_environments "${PROFILE}"
